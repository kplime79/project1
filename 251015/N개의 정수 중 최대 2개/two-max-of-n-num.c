#include <stdio.h>

int main() {
    int a[100] = {0}; // 배열 초기화
    int n;            // 입력할 숫자의 개수

    // 1. n을 입력받고 배열에 n개의 숫자 입력
    scanf("%d", &n);
    for (int i = 0; i < n; i++) {
        scanf("%d", &a[i]);
    }
    
    // =======================================================
    // 2. 삽입 정렬 (Insertion Sort) 로직 - 내림차순으로 수정
    // =======================================================
    // i는 정렬되지 않은 부분의 첫 번째 원소를 가리킵니다. (i=1부터 시작)
    for (int i = 1; i < n; i++) {
        // 현재 원소(a[i])를 Key로 선택하고 임시 저장
        int key = a[i];
        
        // j는 정렬된 부분의 마지막 원소를 가리킵니다.
        int j = i - 1;
        
        // **수정된 비교 조건:**
        // 정렬된 원소(a[j])가 Key보다 '작으면', 뒤로 한 칸 이동(Shift)합니다.
        // (내림차순이므로 큰 값이 앞에 있어야 합니다.)
        while (j >= 0 && a[j] < key) { // a[j]가 key보다 작다면 -> key가 더 크므로 앞으로 와야 함
            a[j + 1] = a[j]; // 원소(a[j])를 뒤로 한 칸 이동
            j = j - 1;       // 다음 원소(왼쪽)로 이동
        }
        
        // 탐색이 끝난 후, Key의 올바른 위치(j+1)에 Key 값을 '삽입'합니다.
        a[j + 1] = key;
    }
    
    printf("%d %d",a[0],a[1]);
    printf("\n");
    
    return 0;
}